def divide(a, b):
    """
    Divide Two Numbers

    :param a: (float) numerator
    :param b: (float) denominator
    :return: (float) result of division
    """
    return a/b


def divide_and_floor(a, b):
    """
    Divide Two Numbers

    :param a: (float) numerator
    :param b: (float) denominator
    :return: (int) result of divide and floor
    """
    return int(divide(a, b))

