def add_two_nums(a, b):
    """
    add two numbers
    :param a: (float) first number
    :param b: (float) second number
    :return: (float) addition result
    """
    return a + b

def subtract(a, b):
    """
    subtract two numbers
    :param a: (float) first number
    :param b: (float) second number
    :return: (float) addition result
    """
    return a - b